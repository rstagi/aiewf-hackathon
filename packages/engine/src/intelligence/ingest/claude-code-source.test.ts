describe("classifyEntry — corpus-spec keep/drop rules", () => {
  for (const c of CASES) {
    it(c.name + " → " + c.expect, () => {
      const result = classifyEntry(c.line);
      expect(result.kind).toBe(c.expect);
      if (c.contains) expect(result.content).toContain(c.contains);
    });
  }

  it("does not false-drop a genuine prompt that merely contains a marker mid-string", () => {
    const r = classifyEntry({ type: "user", message: { role: "user", content: "why does <command-name> show up in my logs?" } });
    expect(r.kind).toBe("user"); // marker is matched as a prefix, not a substring
  });
});

describe("parseClaudeCodeTranscript — ordering, merge, noise", () => {
  it("keeps real turns in file order and drops all the synthetic noise around them", () => {
    const raw = CASES.map((c) => JSON.stringify(c.line)).join("\n");
    const turns = parseClaudeCodeTranscript(raw);
    // Only the user-turn and assistant-turn cases survive (assistant text fragments here have
    // distinct requestIds, so none merge).
    const kept = CASES.filter((c) => c.expect !== "drop");
    expect(turns).toHaveLength(kept.length);
    expect(turns[0]).toMatchObject({ role: "user", index: 0 });
    expect(turns.every((t, i) => t.index === i)).toBe(true);
  });

  it("merges consecutive assistant fragments that share a requestId into one streamed reply", () => {
    const raw = [
      { type: "user", message: { role: "user", content: "do it" } },
      { type: "assistant", requestId: "R1", message: { role: "assistant", content: [{ type: "text", text: "First I'll look." }] } },
      { type: "assistant", requestId: "R1", message: { role: "assistant", content: [{ type: "tool_use", id: "t", name: "Bash", input: {} }] } },
      { type: "user", toolUseResult: { stdout: "ok" }, message: { role: "user", content: [{ type: "tool_result", tool_use_id: "t", content: "ok" }] } },
      { type: "assistant", requestId: "R1", message: { role: "assistant", content: [{ type: "text", text: "Done." }] } },
      { type: "assistant", requestId: "R2", message: { role: "assistant", content: [{ type: "text", text: "A separate later reply." }] } },
    ].map((o) => JSON.stringify(o)).join("\n");
    const turns = parseClaudeCodeTranscript(raw);
    expect(turns.map((t) => t.role)).toEqual(["user", "assistant", "assistant"]);
    expect(turns[1].content).toContain("First I'll look.");
    expect(turns[1].content).toContain("Done."); // merged across the intervening tool_use + tool_result
    expect(turns[2].content).toBe("A separate later reply."); // different requestId → not merged
  });

  it("does not merge assistant turns that both lack a requestId (avoid gluing distinct replies)", () => {
    const raw = [
      { type: "assistant", message: { role: "assistant", content: [{ type: "text", text: "reply one" }] } },
      { type: "assistant", message: { role: "assistant", content: [{ type: "text", text: "reply two" }] } },
    ].map((o) => JSON.stringify(o)).join("\n");
    expect(parseClaudeCodeTranscript(raw)).toHaveLength(2);
  });

  it("tolerates blank and unparseable lines", () => {
    const raw = ["", "{ not json", JSON.stringify({ type: "user", message: { role: "user", content: "hi" } }), "  "].join("\n");
    const turns = parseClaudeCodeTranscript(raw);
    expect(turns).toHaveLength(1);
    expect(turns[0].content).toBe("hi");
  });
});

describe("ClaudeCodeTranscriptSource", () => {
  let root: string;
  const slug = "-Users-me-Dev-proj";
  const sid = "11111111-2222-3333-4444-555555555555";
  beforeAll(() => {
    root = mkdtempSync(join(tmpdir(), "cc-proj-"));
    mkdirSync(join(root, slug), { recursive: true });
    writeFileSync(
      join(root, slug, `${sid}.jsonl`),
      [
        JSON.stringify({ type: "file-history-snapshot", isSnapshotUpdate: true }),
        JSON.stringify({ type: "user", cwd: "/Users/me/Dev/proj", timestamp: "2026-06-23T12:00:00Z", message: { role: "user", content: "make it work" } }),
        JSON.stringify({ type: "assistant", requestId: "R1", timestamp: "2026-06-23T12:00:05Z", message: { role: "assistant", content: [{ type: "text", text: "Done, it works." }] } }),
      ].join("\n"),
    );
    // A dir whose NAME does not equal cwdToSlug(cwd): the recorded cwd has an underscore, which
    // Claude Code dashes in the dir name. projectCwd must recover the real cwd from the file.
    mkdirSync(join(root, "-Users-me-Dev-proj-x"), { recursive: true });
    writeFileSync(
      join(root, "-Users-me-Dev-proj-x", "aaaaaaaa.jsonl"),
      JSON.stringify({ type: "user", cwd: "/Users/me/Dev/proj_x", message: { role: "user", content: "hey" } }),
    );
  });
  afterAll(() => rmSync(root, { recursive: true, force: true }));

  it("lists session ids in a project slug dir", () => {
    expect(new ClaudeCodeTranscriptSource(root).listSessionIds(slug)).toEqual([sid]);
  });

  it("turnsFor parses a session file into clean turns", () => {
    const turns = new ClaudeCodeTranscriptSource(root).turnsFor(slug, sid);
    expect(turns.map((t) => [t.role, t.content])).toEqual([
      ["user", "make it work"],
      ["assistant", "Done, it works."],
    ]);
  });

  it("detailFor returns turns + metadata (cwd, host) and null for a missing session", () => {
    const src = new ClaudeCodeTranscriptSource(root);
    const detail = src.detailFor(slug, sid);
    expect(detail).toMatchObject({ sessionId: sid, host: "claude-code", cwd: "/Users/me/Dev/proj" });
    expect(detail!.turns).toHaveLength(2);
    expect(src.detailFor(slug, "nope")).toBeNull();
  });

  it("locate finds which project slug holds a session id (for open-by-id), null when absent", () => {
    const src = new ClaudeCodeTranscriptSource(root);
    expect(src.locate(sid)).toEqual({ projectSlug: slug });
    expect(src.locate("00000000-0000-0000-0000-000000000000")).toBeNull();
  });

  it("listProjectDirs returns the project directories", () => {
    const dirs = new ClaudeCodeTranscriptSource(root).listProjectDirs();
    expect(dirs).toContain(slug);
    expect(dirs).toContain("-Users-me-Dev-proj-x");
  });

  it("projectCwd recovers the real cwd from a session file, not the lossy dir name", () => {
    const src = new ClaudeCodeTranscriptSource(root);
    // dir name "-Users-me-Dev-proj-x" but the recorded cwd dashes differently (underscore).
    expect(src.projectCwd("-Users-me-Dev-proj-x")).toBe("/Users/me/Dev/proj_x");
    expect(src.projectCwd("does-not-exist")).toBeUndefined();
  });
});
