/**
 * Reads Claude Code's OWN session transcripts under `<projectsRoot>` (default
 * `~/.claude/projects/<dash-cwd>/<sessionId>.jsonl`) and maps them to the engine's `Turn` shape.
 *
 * This is the source-of-truth conversation record, used instead of the lossy `~/.ratel/chat`
 * capture: the ratel `Stop` hook drops the final assistant turn of a session (memory:
 * `ratel-chat-capture-drops-final-turn`), which is exactly the turn resolution judging needs.
 * Claude Code's transcript has the complete conversation.
 *
 * The trade-off is format: CC transcripts are a rich, noisy stream (tool_use / tool_result /
 * thinking blocks, slash-command + meta wrappers, sidechain subagent dialogue, compaction
 * recaps, and ~15 non-conversational entry types). The extraction rules below come from a
 * corpus-wide survey of ~1890 real sessions (see `claude-code-source.test.ts`), so format drift
 * surfaces as a failing test rather than silent data loss. Framework-free: node builtins only,
 * `projectsRoot` injected so the homedir coupling stays in the route layer.
 */

import { readFileSync, readdirSync, existsSync, statSync, openSync, readSync, closeSync } from "node:fs";
import { join } from "node:path";
import type { ChatRole } from "@sia/contract";
import type { Turn } from "../types";
import type { ChatDetail } from "./chat-source";

/**
 * Prefixes that mark a `type:"user"` STRING as synthetic (slash-command / local-command / hook /
 * notification / compaction wrappers, interrupt sentinels, image placeholders). Matched with
 * `startsWith` on the trimmed content — these synthetic strings always BEGIN with the marker, so
 * a genuine prompt that merely mentions one mid-sentence is preserved.
 */
const DROP_USER_PREFIXES = [
  "<command-name>",
  "<command-message>",
  "<command-args>",
  "<command-contents>",
  "<local-command-stdout>",
  "<local-command-stderr>",
  "<local-command-caveat>",
  "Caveat: The messages below were generated by the user",
  "<bash-input>",
  "<bash-stdout>",
  "<bash-stderr>",
  "<task-notification>",
  "<system-reminder>",
  "<user-prompt-submit-hook>",
  "[Request interrupted by user", // un-terminated: also catches "...by user for tool use]"
  "Stop hook feedback:",
  "Base directory for this skill:",
  "[Image: source:",
  "[Image: original ",
  "This session is being continued from a previous conversation",
  "Continue from where you left off.",
  "Continue the conversation from where it left off",
];

interface CCEntry {
  type?: unknown;
  message?: { role?: unknown; content?: unknown };
  requestId?: unknown;
  timestamp?: unknown;
  cwd?: unknown;
  isMeta?: unknown;
  isSidechain?: unknown;
  isCompactSummary?: unknown;
  isVisibleInTranscriptOnly?: unknown;
  isApiErrorMessage?: unknown;
  toolUseResult?: unknown;
  promptSource?: unknown;
  origin?: { kind?: unknown };
  error?: unknown;
}

type Classification = { kind: "user" | "assistant" | "drop"; content: string };
const DROP: Classification = { kind: "drop", content: "" };

/** Join the `text` blocks of an assistant/user content array (skips thinking/tool_use/image/etc.). */
function joinTextBlocks(content: unknown): string {
  if (!Array.isArray(content)) return "";
  return content
    .filter((b): b is { text: string } => !!b && typeof b === "object" && (b as { type?: unknown }).type === "text" && typeof (b as { text?: unknown }).text === "string")
    .map((b) => b.text)
    .join("\n");
}

/**
 * Classify a single parsed transcript entry into a kept user/assistant turn or `drop`. Encodes the
 * corpus-spec rules; pure, so the whole drop/keep matrix is unit-tested line-by-line. Streaming
 * fragment merge (by requestId) is handled by `parseClaudeCodeTranscript`, not here.
 */
export function classifyEntry(raw: unknown): Classification {
  if (!raw || typeof raw !== "object") return DROP;
  const e = raw as CCEntry;
  const msg = e.message;
  // A line with no `message` is non-conversational metadata (system, file-history-snapshot, …).
  if (!msg || typeof msg !== "object") return DROP;
  // Subagent/Task dialogue — the main thread already represents it as a tool_use(Task) pair.
  if (e.isSidechain === true) return DROP;

  if (e.type === "user" && msg.role === "user") {
    if (e.toolUseResult !== undefined) return DROP; // synthetic tool-result entry
    if (e.isMeta === true) return DROP;
    if (e.isCompactSummary === true || e.isVisibleInTranscriptOnly === true) return DROP;
    if (e.promptSource === "system") return DROP;
    if (e.origin && e.origin.kind === "task-notification") return DROP;

    const content = msg.content;
    if (Array.isArray(content)) {
      // A tool_result block anywhere means this is a synthetic tool output, not a human turn.
      if (content.some((b) => b && typeof b === "object" && (b as { type?: unknown }).type === "tool_result")) return DROP;
    }
    const text = (typeof content === "string" ? content : joinTextBlocks(content)).trim();
    if (text.length === 0) return DROP;
    if (DROP_USER_PREFIXES.some((p) => text.startsWith(p))) return DROP;
    return { kind: "user", content: text };
  }

  if (e.type === "assistant" && msg.role === "assistant") {
    if (e.isApiErrorMessage === true || e.error !== undefined) return DROP; // synthetic harness stub
    const text = (typeof msg.content === "string" ? msg.content : joinTextBlocks(msg.content)).trim();
    if (text.length === 0) return DROP; // pure thinking/tool_use/fallback — no human-facing text
    return { kind: "assistant", content: text };
  }

  return DROP;
}

function requestIdOf(raw: unknown): string | undefined {
  const e = raw as CCEntry & { message?: { id?: unknown } };
  const r = e?.requestId ?? e?.message?.id;
  return typeof r === "string" ? r : undefined;
}

function tsOf(raw: unknown): number | undefined {
  const ts = (raw as CCEntry)?.timestamp;
  if (typeof ts !== "string") return undefined;
  const ms = Date.parse(ts);
  return Number.isNaN(ms) ? undefined : ms;
}

/**
 * Parse a full session transcript into clean conversational turns, in file order. Consecutive
 * assistant entries sharing a requestId are one streamed reply, so they're merged into a single
 * turn (Claude Code splits one response across several lines around its tool calls).
 */
export function parseClaudeCodeTranscript(raw: string): Turn[] {
  const acc: { role: ChatRole; content: string; requestId?: string; ts?: number }[] = [];
  for (const line of raw.split("\n")) {
    const trimmed = line.trim();
    if (trimmed.length === 0) continue;
    let obj: unknown;
    try {
      obj = JSON.parse(trimmed);
    } catch {
      continue;
    }
    const c = classifyEntry(obj);
    if (c.kind === "drop") continue;

    if (c.kind === "assistant") {
      const reqId = requestIdOf(obj);
      const last = acc[acc.length - 1];
      if (last && last.role === "assistant" && reqId !== undefined && last.requestId === reqId) {
        last.content = `${last.content}\n${c.content}`; // merge streamed fragment
        continue;
      }
      acc.push({ role: "assistant", content: c.content, requestId: reqId, ts: tsOf(obj) });
    } else {
      acc.push({ role: "user", content: c.content, ts: tsOf(obj) });
    }
  }
  return acc.map((t, index) => {
    const turn: Turn = { role: t.role, content: t.content, index };
    if (t.ts !== undefined) turn.ts = t.ts;
    return turn;
  });
}

/** Scan a raw transcript for lightweight metadata (first cwd, last timestamp). */
function transcriptMeta(raw: string): { cwd?: string; updatedAt?: string } {
  let cwd: string | undefined;
  let updatedAt: string | undefined;
  for (const line of raw.split("\n")) {
    const t = line.trim();
    if (t.length === 0) continue;
    let o: CCEntry;
    try {
      o = JSON.parse(t) as CCEntry;
    } catch {
      continue;
    }
    if (cwd === undefined && typeof o.cwd === "string") cwd = o.cwd;
    if (typeof o.timestamp === "string") updatedAt = o.timestamp; // last one wins
  }
  return { cwd, updatedAt };
}

/** Read up to `maxBytes` from the start of a file (for cheaply sniffing a transcript's `cwd`). */
function readHead(path: string, maxBytes: number): string {
  let fd: number | undefined;
  try {
    fd = openSync(path, "r");
    const buf = Buffer.alloc(maxBytes);
    const n = readSync(fd, buf, 0, maxBytes, 0);
    return buf.toString("utf8", 0, n);
  } catch {
    return "";
  } finally {
    if (fd !== undefined) closeSync(fd);
  }
}

export class ClaudeCodeTranscriptSource {
  constructor(private readonly projectsRoot: string) {}

  /** Project directory names under the CC projects root (each is one dash-encoded cwd). */
  listProjectDirs(): string[] {
    if (!existsSync(this.projectsRoot)) return [];
    return readdirSync(this.projectsRoot, { withFileTypes: true })
      .filter((e) => e.isDirectory())
      .map((e) => e.name);
  }

  /**
   * The absolute `cwd` a project dir was recorded under, sniffed from the head of one of its
   * session files. Claude Code's dir name is a LOSSY encoding of the cwd (it dashes `.`/`_`/spaces
   * and can double-dash), so callers must match on this real cwd rather than on the dir name.
   */
  projectCwd(projectDir: string): string | undefined {
    const dir = join(this.projectsRoot, projectDir);
    if (!existsSync(dir)) return undefined;
    const file = readdirSync(dir).find((f) => f.endsWith(".jsonl"));
    if (!file) return undefined;
    const m = readHead(join(dir, file), 65_536).match(/"cwd"\s*:\s*"((?:[^"\\]|\\.)*)"/);
    if (!m) return undefined;
    try {
      return JSON.parse(`"${m[1]}"`) as string;
    } catch {
      return undefined;
    }
  }

  /** Session ids (filenames) under a project slug dir, most-recently-modified first. */
  listSessionIds(projectSlug: string): string[] {
    const dir = join(this.projectsRoot, projectSlug);
    if (!existsSync(dir)) return [];
    return readdirSync(dir)
      .filter((f) => f.endsWith(".jsonl"))
      .map((f) => ({ id: f.slice(0, -".jsonl".length), mtime: this.mtimeOf(join(dir, f)) }))
      .sort((a, b) => b.mtime - a.mtime)
      .map((x) => x.id);
  }

  private mtimeOf(path: string): number {
    try {
      return statSync(path).mtimeMs;
    } catch {
      return 0;
    }
  }

  /** Parsed turns for one session, or `[]` when the file is absent/empty. */
  turnsFor(projectSlug: string, sessionId: string): Turn[] {
    const path = join(this.projectsRoot, projectSlug, `${sessionId}.jsonl`);
    if (!existsSync(path)) return [];
    return parseClaudeCodeTranscript(readFileSync(path, "utf8"));
  }

  /** One session's turns + metadata for the debug viewer, or `null` when no turns are found. */
  detailFor(projectSlug: string, sessionId: string): ChatDetail | null {
    const path = join(this.projectsRoot, projectSlug, `${sessionId}.jsonl`);
    if (!existsSync(path)) return null;
    const raw = readFileSync(path, "utf8");
    const turns = parseClaudeCodeTranscript(raw);
    if (turns.length === 0) return null;
    const { cwd, updatedAt } = transcriptMeta(raw);
    return { sessionId, host: "claude-code", cwd, updatedAt, turns };
  }

  /** Find which project slug holds a session id (sessions are uuids, so first match wins). */
  locate(sessionId: string): { projectSlug: string } | null {
    if (!existsSync(this.projectsRoot)) return null;
    for (const slug of readdirSync(this.projectsRoot)) {
      if (existsSync(join(this.projectsRoot, slug, `${sessionId}.jsonl`))) return { projectSlug: slug };
    }
    return null;
  }
}
